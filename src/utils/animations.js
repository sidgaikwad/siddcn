// Animation frame calculator
export class AnimationController {
  constructor(frames, fps = 10) {
    this.frames = frames;
    this.fps = fps;
    this.currentFrame = 0;
  }

  next() {
    this.currentFrame = (this.currentFrame + 1) % this.frames.length;
    return this.frames[this.currentFrame];
  }

  reset() {
    this.currentFrame = 0;
  }

  getCurrentFrame() {
    return this.frames[this.currentFrame];
  }
}

